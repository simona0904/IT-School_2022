# GIT CLI

# Comenzi utile in GIT:

# Initializare repo:
# git init

# clone repo = clonare cod:
# git clone URL

# Verificare stare:
# git status 

# Adaugare fisiere pt a fi incluse in urm commit:
# git add NUME FISIER

# Adaugare toate fisierele:
# git add

# Stergere fisiere din lista de modificari pt commit:
# git rm --cached NUME FISIER

# commit = salveaza starea curenta
# git commit -m "Mesaj"
# git commit
# se iese din editor cu ESC > : > wq > enter

# afiseaza configuratia:
# git config --global -l

# configureaza email si nume:
# git config --global user.email "EMAIL GITHUB"
# git config --global user.name "Nume prenume"

# afiseaza istorie:
# git log

# intoarcere in timp - reset la commit:
# git reset COMMIT_ID

# Uploadam modificari: git push
# Descarcam cele mai noi modificari: git pull







